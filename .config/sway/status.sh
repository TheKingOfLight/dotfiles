#
#________________________________________________________________
# Get the Information
#

cpu_usage = $(top -bn1 | grep "CPU(s)" | awk '{print $2 + $4}')
ram_usage = $(free -g | awk '/^Mem:/{print $3}')

date = $(date +"%Y-%m-%d")
time = $(date +"%H:%M")


#
#________________________________________________________________
# Writing to bar
#

echo "$cpu_usage|$ram_usage|$date $time"


